package theater;

/**
 * Represents a theater play, including its name and its type
 * (for example, "tragedy" or "comedy").
 */
public class Play {

    private String name;
    private String type;

    public Play(String name, String type) {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }
}
